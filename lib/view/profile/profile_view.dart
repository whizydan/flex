  Future<void> _fetchUserData() async {
    User? user = FirebaseAuth.instance.currentUser;
    if (user != null) {
      DatabaseReference userRef = FirebaseDatabase.instance.ref().child('profiles').child(user.uid);
      DatabaseReference usersRef = FirebaseDatabase.instance.ref().child('users').child(user.uid);
      DataSnapshot snapshot = await userRef.get();
      DataSnapshot snapshotUser = await usersRef.get();

      if (snapshot.exists) {
        Map userData = snapshot.value as Map;
        setState(() {
          firstName = userData['firstName'] ?? "";
          lastName = userData['lastName'] ?? "";
          height = userData['height']?.toString() ?? "";
          weight = userData['weight']?.toString() ?? "";
          age = userData['dateOfBirth']?.toString() ?? "";
        });
      }
      if (snapshotUser.exists) {
        Map userData = snapshotUser.value as Map;
        setState(() {
          firstName = userData['firstName'] ?? "";
          lastName = userData['lastName'] ?? "";
        });
      }
    }
  }

